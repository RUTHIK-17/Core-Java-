package com.ruthik.www;

public class NumberConv {

	public static void main(String[] args) {
		int c = 045;
		System.out.println(c);		//Octal number
		
		int a = 45;
		System.out.println(a);		//Decimal
		
		int b = 0x45;
		System.out.println(b);		//Hexadecimal
		
		int d = 0b101100; 
		System.out.println(d);		//binary

	}

}
